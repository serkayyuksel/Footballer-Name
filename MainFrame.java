    private void btnAddSongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddSongMouseClicked

        int selectedDVD = jDVDList.getSelectedIndex();

        if (selectedDVD != -1) {
            if(fieldSong.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Please, fill out a song's name");
            else
            DefaultListModelSongs.addElement(fieldSong.getText());
            dataManipulator.getLinkedList().getAt(selectedDVD).getSongs().add(" " + fieldSong.getText());
            fieldSong.setText("");
        }
        else {
            JOptionPane.showMessageDialog(null, "Please, select a DVD first");
        }

    }//GEN-LAST:event_btnAddSongMouseClicked

