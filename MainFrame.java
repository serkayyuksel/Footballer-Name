    private void btnSortMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSortMouseClicked
        btnSort.setText(btnSort.getText()=="Z-A"?"A-Z":"Z-A");

        if(btnSort.getText() == "A-Z")
            dataManipulator.inventoryToListAsc(jDVDList, DefaultListModelDvd);
        else {
            dataManipulator.inventoryToListDesc(jDVDList, DefaultListModelDvd);
        }
    }//GEN-LAST:event_btnSortMouseClicked

    private void btnAddDVDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddDVDMouseClicked
        String singerName = fieldSinger.getText();
        String dvdTitle = fieldDVDTitle.getText();
        boolean success = false;

        if(singerName.equals("") || dvdTitle.equals("")) {
             JOptionPane.showMessageDialog(null, "Please, fill out all data");
             return;
        }
            
        
        if(success) {
            Dvd dvd = new Dvd(singerName, dvdTitle);
            dataManipulator.getLinkedList().add(dvd);
            fieldSinger.setText("");
            fieldDVDTitle.setText("");

            if(btnSort.getText() == "A-Z")
                dataManipulator.inventoryToListAsc(jDVDList, DefaultListModelDvd);
            else {
                dataManipulator.inventoryToListDesc(jDVDList, DefaultListModelDvd);
            }
        }
            
    }//GEN-LAST:event_btnAddDVDMouseClicked

