    private void jDVDListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jDVDListValueChanged
        int index = jDVDList.getSelectedIndex();
        dataManipulator.getLinkedList().iterateBackward();
        
        if (index != -1) {
            Dvd dvd = dataManipulator.getLinkedList().getAt(index);
            DefaultListModelSongs.clear();

            if(dvd.getSongs().isEmpty() == false) {
                for(String s : dvd.getSongs()) {
                     DefaultListModelSongs.addElement(s);
                }
            }

        }
        else {
            DefaultListModelSongs.clear();
        }
    }//GEN-LAST:event_jDVDListValueChanged

    private void btnRemoveDVDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveDVDActionPerformed
        int selectedIndex = jDVDList.getSelectedIndex();

        if (selectedIndex != -1) {
            dataManipulator.getLinkedList().removeAt(selectedIndex);
            DefaultListModelDvd.remove(selectedIndex);
        }
    }//GEN-LAST:event_btnRemoveDVDActionPerformed

